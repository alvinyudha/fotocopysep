<script>
    $('.form-check-input').on('click', function() { // jquery mencari element yang kelasnya form check input
        const menuId = $(this).data('menu'); //
        const roleId = $(this).data('role');

        $.ajax({
            url: "<?= base_url('admin/changeaccess') ?>", //akan mengarah ke controller ini dengan mengirim dua data
            type: 'post',
            data: {
                menuId: menuId, //yg satu objek data yg satu lagi adalah variabel yg diambil dari checkbox
                roleId: roleId
            },
            success: function() { //setelah success akan diarahkan dengan redirect menggunakan javascript
                document.location.href = "<?= base_url('admin/roleaccess/'); ?>" + roleId;
            }
        });
    });
</script>
<script>
    function lihatGambar(input) {
        if (input.files && input.files[0]) {
            var reader = new FileReader();
            reader.onload = function(e) {
                $('#gambar_load').attr('src', e.target.result);
            }
            reader.readAsDataURL(input.files[0]);
        }
    }
    $('#preview_gambar').change(function() {
        lihatGambar(this);
    });
</script>
